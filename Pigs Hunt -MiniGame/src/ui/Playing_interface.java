    private void btn_trg1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_trg1ActionPerformed
        if ("E".equals(lb_gamemode.getText())) {
            currentscore = 10;
        }
        if ("N".equals(lb_gamemode.getText())) {
            currentscore = 15;
        }
        if ("H".equals(lb_gamemode.getText())) {
            currentscore = 30;
        }
        int i = currentscore;
        int k = Integer.valueOf(txt_score.getText());
        int val = i + k;
        txt_score.setText(String.valueOf(val));
        target_pig1.setVisible(false);

        int a = Integer.valueOf(lb_ammo.getText());
        int b = a - 1;
        lb_ammo.setText(String.valueOf(b));

        if (a == 0) {
            lb_ammo.setText("0");
            btn_trg1.setEnabled(false);
            btn_trg2.setEnabled(false);
            btn_trg3.setEnabled(false);
            btn_trg4.setEnabled(false);
            btn_trg5.setEnabled(false);
            btn_trg6.setEnabled(false);
            lb_bullet.setVisible(false);
        }
        int bl = Integer.valueOf(lb_ammo.getText());
        if (bl == 0) {
            click_Empty();

        } else {
            gunclick_sound();
            pig_kill();
        }
    }//GEN-LAST:event_btn_trg1ActionPerformed

