    private void countdown() {

        new Thread(new Runnable() {

            @Override
            public void run() {
                try {

                    for (int j = 60; j >= 0; j--) {
                        txt_time.setText("Time Remaining " + j);

                        if (j == 0) {

                            Score_panel sp = new Score_panel(null, true);
                            winSound();
                            sp.setVisible(true);

                        }
                        Thread.sleep(1000);
                        String vl = HomePage.lb_thread_stoping_value.getText();
                        if ("1".equals(vl)) {
                            Thread.currentThread().stop();
                        }

                    }
                    Thread.currentThread().stop();

                } catch (InterruptedException e) {
                }

            }
        }).start();
    }

